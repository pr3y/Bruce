void disPlayBLESend()
{
    uint8_t senddata[2] = {0};

    pService->start();
    pServer->getAdvertising()->start();

    uint64_t chipid = ESP.getEfuseMac();
    String blename = "M5-" + String((uint32_t)(chipid >> 32), HEX);

    tft.setTextSize(1);

    bool wasConnected = true;
    while (!checkEscPress())
    {
        if (deviceConnected)
        {
            if (!wasConnected) {
                tft.fillRect(0, 0, 240, 135, TFT_BLACK);
                tft.pushImage(180, 16, 48, 48, (uint16_t *)icon_ble);
                tft.setTextColor(tft.color565(180, 180, 180));
                tft.setTextSize(3);
                tft.setCursor(12, 20);
                // tft.printf("BLE connect!\n");
                tft.printf("BLE Send\n");
                tft.setTextSize(5);
            }
            tft.fillRect(10, 70, 240, 50, TFT_BLACK);
            tft.setCursor(12, 75);
            if (senddata[0] % 4 == 0)
            {
                tft.printf("0x%02X>  ", senddata[0]);
            }
            else if (senddata[0] % 4 == 1)
            {
                tft.printf("0x%02X>>", senddata[0]);
            }
            else if (senddata[0] % 4 == 2)
            {
                tft.printf("0x%02X >>", senddata[0]);
            }
            else if (senddata[0] % 4 == 3)
            {
                tft.printf("0x%02X  >", senddata[0]);
            }

            senddata[1]++;
            if (senddata[1] > 3)
            {
                senddata[1] = 0;
                senddata[0]++;
                pTxCharacteristic->setValue(senddata, 1);
                pTxCharacteristic->notify();
            }
            wasConnected = true;
        }
        else
        {
            if (wasConnected) {
                tft.fillRect(0, 0, 240, 135, TFT_BLACK);
                tft.setTextSize(2);
                tft.setCursor(12, 20);
                tft.setTextColor(TFT_RED);
                tft.printf("BLE disconnect\n");
                tft.setCursor(12, 45);
                tft.setTextColor(tft.color565(18, 150, 219));

                tft.printf(String("Name:" + blename + "\n").c_str());
                tft.setCursor(12, 70);
                tft.printf("UUID:1bc68b2a\n");
                tft.pushImage(180, 16, 48, 48, (uint16_t *)icon_ble_disconnect);
            }
            wasConnected = false;
        }
        delay(200);
    }

    tft.setTextColor(TFT_WHITE);
    pService->stop();
    pServer->getAdvertising()->stop();
}

